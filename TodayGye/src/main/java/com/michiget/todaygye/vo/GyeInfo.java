package com.michiget.todaygye.vo;

public class GyeInfo {
	
	String userId;
	String openCheck;
	String gyeName;
	String persons;
	String money;
	String cycle;
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getOpenCheck() {
		return openCheck;
	}
	public void setOpenCheck(String openCheck) {
		this.openCheck = openCheck;
	}
	public String getGyeName() {
		return gyeName;
	}
	public void setGyeName(String gyeName) {
		this.gyeName = gyeName;
	}
	public String getPersons() {
		return persons;
	}
	public void setPersons(String persons) {
		this.persons = persons;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public String getCycle() {
		return cycle;
	}
	public void setCycle(String cycle) {
		this.cycle = cycle;
	}
	
}
